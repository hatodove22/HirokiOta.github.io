import puppeteer from 'puppeteer';
const browser = await puppeteer.launch({headless:true,args:['--no-sandbox']});
const page = await browser.newPage();
page.on('response', async r=>{ if(r.status()>=400) console.log('HTTP', r.status(), r.url()); });
page.on('console', m=>console.log('CONSOLE', m.type(), m.text()));
await page.goto('http://localhost:5174',{waitUntil:['domcontentloaded','networkidle0']});
await page.screenshot({path:'tmp/proto-home.png', fullPage:true});
const txt = await page.evaluate(()=>document.body.innerText.slice(0,200));
console.log('SNIPPET\n'+txt);
await browser.close();
